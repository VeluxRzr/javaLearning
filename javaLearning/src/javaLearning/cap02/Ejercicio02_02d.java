public class Ejercicio02_02d {
	public static void main(String[] args) {
		int n = 7;
		System.out.println("1) n: " + n);//n vale 7
		System.out.println("2) ++n:" + ++n);//incremento n y luego lo muestro, al salir n vale 8
		System.out.println("3) n: " + n);//aqui n vale 8
		System.out.println("4) n++: " + n++);//aqui me muestra el valor de n que es 8 y luego le suma 1
		System.out.println("5) n: " + n);//aqui muestra el valor de n que es 9
	}
}